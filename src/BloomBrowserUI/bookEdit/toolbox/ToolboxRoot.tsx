// React host for the toolbox sidebar.
//
// This component bridges old and new toolbox systems while migration is in progress. It was generated by Copilot
// to allow the root of the toolbox to be rendered in React while still supporting several tools that have not yet
// been migrated to React. The goal is to eventually remove all legacy toolbox code and have a fully React-managed toolbox.
// The descriptions and plans below were also generated by Copilot and may or may not be helpful.
// - it renders accordion sections in React,
// - reuses already-instantiated legacy tool DOM when available,
// - falls back to loading legacy HTML snippets when live DOM is not ready,
// - and exposes a small adapter so legacy code can still control active tool state.
//
// Completed migration target:
// - React is the single owner of toolbox state and rendering,
// - each tool is rendered through a React component/factory (no legacy HTML injection),
// - no DOM adoption from legacy #toolbox roots,
// - no hydration polling or legacy add/remove bridge events,
// - and toolbox globals are reduced to a minimal compatibility surface (or removed).
//
// Practical completion checklist for this file:
// - remove legacy HTML fallback loading/injection paths,
// - remove existing legacy element adoption paths,
// - replace legacy event synchronization with direct React/store updates,
// - remove hidden legacy #toolbox root handling,
// - then simplify comments to describe a fully React-managed toolbox.

type ToolboxSection = {
    id: string;
    label: string;
    legacyToolHtmlSubPath?: string;
    legacyToolBodyHtml?: string;
    liveToolBodyElement?: HTMLDivElement;
};

